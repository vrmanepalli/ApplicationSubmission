/*
 * UserRequest utility class maitains the user request details.
 */

package com.nike.util;


public class UserRequest {

	private String projectFolderUrl;
	private String imageFolderUrl;

	public UserRequest(String projectFolderUrl, String imageFolderUrl) {
		this.imageFolderUrl = imageFolderUrl;
		this.projectFolderUrl = projectFolderUrl;
	}

	public String getProjectFolderUrl() {
		return projectFolderUrl;
	}

	public void setProjectFolderUrl(String projectFolderUrl) {
		this.projectFolderUrl = projectFolderUrl;
	}

	public String getImageFolderUrl() {
		return imageFolderUrl;
	}

	public void setImageFolderUrl(String imageFolderUrl) {
		this.imageFolderUrl = imageFolderUrl;
	}

}
