package com.nike.util;

public class ParametersObject {
	
	private String AppTitle;

	public String getAppTitle() {
		return AppTitle;
	}

	public void setAppTitle(String appTitle) {
		AppTitle = appTitle;
	}
	
	private String isPush;

	public String getIsPush() {
		return isPush;
	}

	public void setIsPush(String isPush) {
		this.isPush = isPush;
	}
	
	private String appRequestType;

	public String getAppRequestType() {
		return appRequestType;
	}

	public void setAppRequestType(String appRequestType) {
		this.appRequestType = appRequestType;
	}
	
	private String version;

	public String getVersion() {
		return version;
	}

	public void setVersion(String version) {
		this.version = version;
	}
	
	private String finalFolderName;
	private String bundleID;

	public String getFinalFolderName() {
		return finalFolderName;
	}

	public void setFinalFolderName(String finalFolderName) {
		this.finalFolderName = finalFolderName;
	}

	public void setBundleID(String bundleID) {
		this.bundleID = bundleID;
	}
	
	public String getBundleID() {
		return bundleID;
	}


}
